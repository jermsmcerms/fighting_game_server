package library;

public enum GGPOErrorCode {
    GGPO_OK,
    GGPO_ERRORCODE_SUCCESS,
    GGPO_ERRORCODE_GENERAL_FAILURE,
    GGPO_ERRORCODE_INVALID_SESSION,
    GGPO_ERRORCODE_INVALID_PLAYER_HANDLE,
    GGPO_ERRORCODE_PLAYER_OUT_OF_RANGE,
    GGPO_ERRORCODE_PREDICTION_THRESHOLD,
    GGPO_ERRORCODE_UNSUPPORTED,
    GGPO_ERRORCODE_NOT_SYNCHRONIZED,
    GGPO_ERRORCODE_IN_ROLLBACK,
    GGPO_ERRORCODE_INPUT_DROPPED,
    GGPO_ERRORCODE_PLAYER_DISCONNECTED,
    GGPO_ERRORCODE_TOO_MANY_SPECTATORS,
    GGPO_ERRORCODE_INVALID_REQUEST;

    public static boolean GGPOSucceeded(GGPOErrorCode result) {
        return result == GGPO_ERRORCODE_SUCCESS || result == GGPO_OK;
    }
}
